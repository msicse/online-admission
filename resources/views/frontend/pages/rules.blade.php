        <h4 style="font-style: normal;color: #4a148c">Grade Report</h4>
        <p style="font-style:normal;color: #0D0A0A; font-size:15px"> Each student’s grade point average is numerical value obtained by
            dividing the total grade points earned by the credits attempted. Only the
            courses graded A+, A, A-, B+, B, B-, C+, C, D and F are used to determine
            credits attempted. In case students repeat courses, GPA and CGPA will be
            calculated on the basis of the grades obtained at the last attempt of the course
            (S) only. Grades obtained in course (s) in all examinations will be shown in the grade
            report. In addition, students who complete courses in addition to their normal credit
            requirements for graduation will inform the Registrar in writing about the course,
            which he intends to declare for consideration towards the requirements for the degree.</p>


        <h4 style="font-style: normal;color: #0b2e13"> The Incomplete:</h4>
        <p style="font-style:normal;color: #0D0A0A; font-size:15px">The ‘Incomplete’ (I) grade is used in special circumstances.
            The ‘Incomplete’ may be given only at the end of a semester to
            a student who has completed all other requirements except the final examination.

            The student has the responsibility to take the initiative in making up the
            Incomplete as specified by the instructor. If action is not taken within three
            weeks of commencement of the next semester, the ‘I’ grade will automatically be
            converted into “F”. In the event, where the instructor from whom a student received an
            incomplete grade is not available, the disposition of the case-involving grade rests with
            the Dean of Faculties.</p>

        <h4 style="font-style: normal;color: #0b2e13">Academic Probation:</h4>
        <p style="font-style:normal;color: #0D0A0A; font-size:15px">A student must maintain a good academic standing with a
            CGPA of at least 2.00. Students with CGPA and (or) GPA of less
            than 2.00 will be placed on Probation. Students on Probation will be allowed a
            maximum of three semesters to earn CGPA and (or) GPA 2.00 or more failing of which
            will result in dropped from the program.</p>

        <h4 style="font-style: normal;color: #0b2e13">Retake Policy:</h4>
        <p style="font-style:normal;color: #0D0A0A; font-size:15px">A course passed with a grade less than B may be repeated to improve
            the relevant grade. When a course is repeated, the previous grade will be automatically
            cancelled. Students, failing in a course, will be allowed to repeat that course twice at the
            most on payment of course fee.</p>


        <h4 style="font-style: normal;color: #0b2e13">Appeal for Grade:</h4>
        <p style="font-style:normal;color: #0D0A0A; font-size:15px">A student not satisfied with the grade in any course may appeal to the course teacher.
            But if the grade is not improved and at the same time, the student is not satisfied with
            the explanation of the teacher concerned, he/ she may make a written request to the Chair
            of the department or Dean of the faculty. The Chair will review the process by which the grade
            was determined. After having the grade or answer script-rechecked, the revised final grade will
            be delivered. These procedures must take place within two weeks after the grade was published. Before
            making an appeal
            the student will have to make a payment of fee as determined by the university authority</p>
